    async function fullscreen(){
      if(document.body.classList.contains('pseudo-fullscreen')){
        document.body.classList.remove('pseudo-fullscreen');
        fit();
        return;
      }
      const root=document.documentElement;
      const enter=root.requestFullscreen||root.webkitRequestFullscreen;
      const leave=document.exitFullscreen||document.webkitExitFullscreen;
      try{
        if(!(document.fullscreenElement||document.webkitFullscreenElement)){
          await enter?.call(root);
          if(!(document.fullscreenElement||document.webkitFullscreenElement))document.body.classList.add('pseudo-fullscreen');
        }
        else await leave?.call(document);
      }catch(error){
        document.body.classList.add('pseudo-fullscreen');
        console.warn('Native fullscreen is unavailable; using presentation focus mode.',error);
      }
      fit();
    }
