    function fit(){
      const {width,height}=viewportSize();
      const focusMode=Boolean(
        document.fullscreenElement||
        document.webkitFullscreenElement||
        document.body.classList.contains('pseudo-fullscreen')
      );
      const topSafe=focusMode?0:54;
      const bottomSafe=0;
      const availableHeight=Math.max(320,height-topSafe-bottomSafe);
      document.documentElement.style.setProperty('--deck-top-safe',topSafe+'px');
      document.documentElement.style.setProperty('--deck-bottom-safe',bottomSafe+'px');
      document.documentElement.style.setProperty('--deck-stage-center',(topSafe+availableHeight/2)+'px');
      document.body.classList.toggle('deck-chrome',!focusMode&&!overview);
      if(overview){
        const cols=overviewColumns(width),gap=10,pad=20;
        const cellWidth=(width-pad-gap*(cols-1))/cols;
        const s=Math.min(.3,cellWidth/1600);
        document.documentElement.style.setProperty('--scale',s);
        document.body.classList.remove('stacked-stage','compact-stage');
        wraps.forEach(w=>{w.style.height=(900*s)+'px'});
        return;
      }
      const portrait=width<=900&&height>width;
      const compact=width<=1100&&(width/height)<1.48;
      const stacked=portrait||compact;
      document.body.classList.toggle('stacked-stage',stacked);
      document.body.classList.remove('compact-stage');
      if(stacked){
        const s=Math.max(0,(width-20)/1600);
        document.documentElement.style.setProperty('--scale',s);
        wraps.forEach(w=>{
          w.style.height=(900*s+topSafe+20)+'px';
        });
      }else{
        const s=Math.max(0,Math.min((width-20)/1600,(availableHeight-10)/900));
        document.documentElement.style.setProperty('--scale',s);
        wraps.forEach(w=>{
          w.style.height=height+'px';
        });
      }
    }
