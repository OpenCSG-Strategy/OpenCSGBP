function main() {
  for (const lang of Object.keys(translations)) {
    const filePath = path.join(i18nDir, `${lang}.json`);
    const pack = JSON.parse(fs.readFileSync(filePath, 'utf8'));
    let updated = 0;
    for (const [en, translated] of Object.entries(translations[lang])) {
      if (pack.phrases[en] === en || !pack.phrases[en]) {
        pack.phrases[en] = translated;
        updated++;
      }
    }
    fs.writeFileSync(filePath, `${JSON.stringify(pack, null, 2)}\n`);
    console.log(`${lang}: updated ${updated} phrases`);
  }

  console.log('\n=== Final Summary ===');
  for (const lang of ['ja','ko','ar','ru','fr','de','es','pt']) {
    const pack = JSON.parse(fs.readFileSync(path.join(i18nDir, `${lang}.json`), 'utf8'));
    const phrases = pack.phrases || {};
    const sameAsKey = Object.entries(phrases).filter(([k, v]) => v && v === k);
    const genuinelyMissing = sameAsKey.filter(([k]) => !keepEnglish.has(k));
    console.log(`${lang}: ${Object.keys(phrases).length} phrases, ${sameAsKey.length} sameAsKey (${genuinelyMissing.length} genuinely untranslated)`);
    if (genuinelyMissing.length > 0) {
      genuinelyMissing.forEach(([k]) => console.log(`  STILL MISSING: ${JSON.stringify(k)}`));
    }
  }
}

main();
